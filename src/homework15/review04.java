package homework15;

import java.util.Scanner;

public class review04 {

	public static void main(String[] args) {
	//	Write a program to print letters from c to m on the console by using do-while loop.
		
		
		char letter = 'c';
		
		do {
			System.out.print(letter + " ");
			letter++;
			
		}while(letter<='m');
	}

}
