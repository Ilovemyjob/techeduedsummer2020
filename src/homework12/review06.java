package homework12;

public class review06 {

	public static void main(String[] args) {
		// Write a program to print counting numbers which are less than 200 and divisible by 5
		//On the console by using for-loop

		for(int i=200; i>0; i--) {
			if(i%5==0) {
				System.out.print(i + " ");
			}
		}
		
	}

}
